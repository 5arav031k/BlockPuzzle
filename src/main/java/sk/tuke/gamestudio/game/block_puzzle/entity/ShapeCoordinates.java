package main.java.sk.tuke.gamestudio.game.block_puzzle.entity;

public class ShapeCoordinates {
    private int minX;
    private int maxX;
    private int minY;
    private int maxY;
    private int firstMinX;
    private int firstMinY;
    public ShapeCoordinates() {
    }
    public int getMinX() {
        return minX;
    }
    public void setMinX(int minX) {
        this.minX = minX;
    }
    public int getMaxX() {
        return maxX;
    }
    public void setMaxX(int maxX) {
        this.maxX = maxX;
    }
    public int getMinY() {
        return minY;
    }
    public void setMinY(int minY) {
        this.minY = minY;
    }
    public int getMaxY() {
        return maxY;
    }
    public void setMaxY(int maxY) {
        this.maxY = maxY;
    }
    public int getFirstMinX() {
        return firstMinX;
    }
    public void setFirstMinX(int firstMinX) {
        this.firstMinX = firstMinX;
    }
    public int getFirstMinY() {
        return firstMinY;
    }
    public void setFirstMinY(int firstMinY) {
        this.firstMinY = firstMinY;
    }
}
